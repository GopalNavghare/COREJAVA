package AkshaySir;

public class Forloop 
{

	public static void main(String[] args)
	{
	    for(int i=1;i<=5;i++)
		{
			System.out.println("Hello");
		}
		
		int j=1;
		do
		{
			System.out.println("Hello..");
			j++;
		}
		while(j<=5);
		
		int i=1;
		while(i<=5)
		{
			System.out.println("hello....");
			i++;
		}
	}

}
